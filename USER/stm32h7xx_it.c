/**
  * @brief This function handles USART3 global interrupt.
  */
void USART7_IRQHandler(void)
{
  /* USER CODE BEGIN USART3_IRQn 0 */

  /* USER CODE END USART3_IRQn 0 */
  HAL_UART_IRQHandler(&huart7);
  /* USER CODE BEGIN USART3_IRQn 1 */
	
		#define RXBUFFERSIZE   1 //缓存大小
		extern uint8_t aRxBuffer3[RXBUFFERSIZE];//HAL库使用的串口接收缓冲
	
		uint32_t timeout=0;
    uint32_t maxDelay=0x1FFFF;
#if SYSTEM_SUPPORT_OS	 	//使用OS
	OSIntEnter();    
#endif

	HAL_UART_IRQHandler(&huart7);	//调用HAL库中断处理公用函数
	
	timeout=0;
    while (HAL_UART_GetState(&huart7)!=HAL_UART_STATE_READY)//等待就绪
	{
        timeout++;////超时处理
        if(timeout>maxDelay) break;		
	}
     
	timeout=0;
	while(HAL_UART_Receive_IT(&huart7,(uint8_t *)aRxBuffer3, RXBUFFERSIZE)!=HAL_OK)//一次处理完成之后，重新开启中断并设置RxXferCount为1
	{
        timeout++; //超时处理
        if(timeout>maxDelay) break;	
	}
#if SYSTEM_SUPPORT_OS	 	//使用OS
	OSIntExit();  											 
#endif

  /* USER CODE END USART3_IRQn 1 */
}

